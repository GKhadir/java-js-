class tarpezoid
{
	static void tar()
	{
		int a=21;
		int b=11;
		int height=5;
		double result=0.5*(a+b)*height;
		System.out.println("area of tarpezoid:"+result);
	}
	public static void main(String[] args) 
	{
		System.out.println("*******main starts******");
		tar();
		System.out.println("*******main ends******");
	}
}







