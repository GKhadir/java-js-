class parallelogram
{
	static void para()
	{

		int base=11;
		int height=5;
		double result=base*height;
		System.out.println("area of parallelogram:"+result);
	}
	public static void main(String[] args) 
	{
		System.out.println("*******main starts******");
		para();
		System.out.println("*******main ends******");
	}
}







